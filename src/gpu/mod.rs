    /// Initialize acceleration estimates by computing forces at initial positions.
    /// Must be called once before the first Verlet step.
    pub fn init_accels(
        &mut self,
        is_weber: bool,
        thrust: Vec3,
        torque_z: Decimal,
    ) -> Result<(), Box<dyn std::error::Error>> {
        if is_weber {
            self.launch_weber_forces()?;
        } else {
            self.launch_newton_forces()?;
        }
        self.launch_bond_forces()?;
        let dd_thrust = DD3::from_vec3(thrust);
        let dd_torque = DD::from_decimal(torque_z);
        self.combine_forces_and_accel(is_weber, dd_thrust, dd_torque)?;
        Ok(())
    }
