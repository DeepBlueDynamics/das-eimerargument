/// Field residual GPU context (separate, uses f64 kernels).
pub struct FieldResidualGpu {
    dev: Arc<CudaDevice>,
    d_probes: CudaSlice<u8>,
    d_sources: CudaSlice<u8>,
    d_results: CudaSlice<u8>,
    n_probes: usize,
    n_sources: usize,
}

impl FieldResidualGpu {
    /// Create field residual GPU context.
    pub fn new(
        probes: &[(f64, f64, f64, f64, f64, usize, f64)], // (x,y,z, theta,phi, layer, d_perp)
        sources: &[(f64, f64, f64, f64, f64, f64)],        // (px,py,pz, dlx,dly,dlz)
    ) -> Result<Self, Box<dyn std::error::Error>> {
        let dev = CudaDevice::new(0)?;

        dev.load_ptx(Ptx::from_src(FIELD_RESIDUAL_PTX), "field_residual", &[
            "field_residual_kernel",
        ])?;

        let gpu_probes: Vec<GpuProbePoint> = probes.iter().map(|&(x,y,z,theta,phi,layer,d_perp)| {
            GpuProbePoint {
                pos: [x, y, z],
                theta,
                phi,
                layer: layer as i32,
                d_perp,
                _pad: 0.0,
            }
        }).collect();

        let gpu_sources: Vec<GpuF64Source> = sources.iter().map(|&(px,py,pz,dlx,dly,dlz)| {
            GpuF64Source {
                pos: [px, py, pz],
                dl: [dlx, dly, dlz],
            }
        }).collect();

        let n_probes = gpu_probes.len();
        let n_sources = gpu_sources.len();

        let d_probes = Self::upload_bytes(&dev, &gpu_probes)?;
        let d_sources = Self::upload_bytes(&dev, &gpu_sources)?;
        let d_results = dev.alloc_zeros::<u8>(n_probes * std::mem::size_of::<GpuProbeResult>())?;

        Ok(FieldResidualGpu {
            dev,
            d_probes,
            d_sources,
            d_results,
            n_probes,
            n_sources,
        })
    }

    fn upload_bytes<T: Copy>(dev: &Arc<CudaDevice>, data: &[T]) -> Result<CudaSlice<u8>, Box<dyn std::error::Error>> {
        let bytes = unsafe {
            std::slice::from_raw_parts(
                data.as_ptr() as *const u8,
                data.len() * std::mem::size_of::<T>(),
            )
        };
        let buf = dev.htod_copy(bytes.to_vec())?;
        Ok(buf)
    }

    /// Compute field residual for all probes at given omega and c_eff_sq.
    pub fn compute(
        &self,
        omega: f64,
        c_eff_sq: f64,
    ) -> Result<Vec<GpuProbeResult>, Box<dyn std::error::Error>> {
        let n_blocks = (self.n_probes as u32 + 256 - 1) / 256;

        let func = self.dev.get_func("field_residual", "field_residual_kernel")
            .ok_or("field_residual_kernel not found")?;

        unsafe {
            func.launch(
                LaunchConfig {
                    grid_dim: (n_blocks, 1, 1),
                    block_dim: (256, 1, 1),
                    shared_mem_bytes: 0,
                },
                (
                    &self.d_probes,
                    &self.d_sources,
                    &self.d_results,
                    omega,
                    c_eff_sq,
                    self.n_probes as i32,
                    self.n_sources as i32,
                ),
            )?;
        }

        let bytes = self.dev.dtoh_sync_copy(&self.d_results)?;
        let results: &[GpuProbeResult] = unsafe {
            std::slice::from_raw_parts(
                bytes.as_ptr() as *const GpuProbeResult,
                self.n_probes,
            )
        };

        Ok(results.to_vec())
    }
}
